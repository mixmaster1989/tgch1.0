            chat_id = task_info["chat_id"]
            message_id = task_info["message_id"]
            bot = task_info["task"]._loop._running_task.get_name().split('_')[0]
            
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
