def generate_post(post_type=None, category=None):
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –ø–æ—Å—Ç –¥–ª—è Telegram-–∫–∞–Ω–∞–ª–∞
    
    Args:
        post_type (str, optional): –¢–∏–ø –ø–æ—Å—Ç–∞ (informational, promotional, educational, entertaining)
        category (str, optional): –ö–∞—Ç–µ–≥–æ—Ä–∏—è –ø–æ—Å—Ç–∞ (tech, business, lifestyle, entertainment)
    
    Returns:
        str: –°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –ø–æ—Å—Ç
    """
    templates = load_templates()
    topics = load_topics()
    content_blocks = load_content_blocks()
    questions = load_questions()
    hashtags = load_hashtags()
    
    # –ï—Å–ª–∏ —Ç–∏–ø –ø–æ—Å—Ç–∞ –Ω–µ —É–∫–∞–∑–∞–Ω, –≤—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π
    if not post_type:
        post_type = random.choice(list(templates.keys()))
    
    # –ï—Å–ª–∏ –∫–∞—Ç–µ–≥–æ—Ä–∏—è –Ω–µ —É–∫–∞–∑–∞–Ω–∞, –≤—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—É—é
    if not category:
        category = random.choice(list(topics.keys()))
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π —à–∞–±–ª–æ–Ω –∏–∑ –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ —Ç–∏–ø–∞
    template = random.choice(templates[post_type])
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—É—é —Ç–µ–º—É –∏–∑ –≤—ã–±—Ä–∞–Ω–Ω–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    topic = random.choice(topics[category])
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π –±–ª–æ–∫ –∫–æ–Ω—Ç–µ–Ω—Ç–∞ –∏–∑ –≤—ã–±—Ä–∞–Ω–Ω–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    content = random.choice(content_blocks[category]).format(topic=topic.lower())
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π –≤–æ–ø—Ä–æ—Å
    question = random.choice(questions).format(topic=topic.lower())
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π —Ö—ç—à—Ç–µ–≥
    hashtag = random.choice(hashtags[category])
    
    # –¢–µ–∫—É—â–∏–π –≥–æ–¥
    year = datetime.now().year
    
    # –§–æ—Ä–º–∏—Ä—É–µ–º –ø–æ—Å—Ç
    post = template.format(
        topic=topic,
        content=content,
        question=question,
        hashtag=hashtag,
        year=year
    )
    
    return post

